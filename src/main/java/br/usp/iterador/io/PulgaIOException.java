package br.usp.iterador.io;

/**
 * Pulga io exception.
 * 
 * @author Guilherme Silveira
 */
public class PulgaIOException extends Exception {

	/**
	 * serial version uid
	 */
	private static final long serialVersionUID = -1075836469805445801L;

	/**
	 * Pulga io exception
	 *
	 */
	public PulgaIOException() {
		super();
	}

	/**
	 * Pulga io exception
	 *
	 */
	public PulgaIOException(String message, Throwable cause) {
		super(message, cause);
	}

	/**
	 * Pulga io exception
	 *
	 */
	public PulgaIOException(String message) {
		super(message);
	}

	/**
	 * Pulga io exception
	 *
	 */
	public PulgaIOException(Throwable cause) {
		super(cause);
	}

}
