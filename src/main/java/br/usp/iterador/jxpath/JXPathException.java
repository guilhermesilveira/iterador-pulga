package br.usp.iterador.jxpath;

public class JXPathException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7449677886605139676L;

	public JXPathException() {
		super();
	}

	public JXPathException(String message, Throwable cause) {
		super(message, cause);
	}

	public JXPathException(String message) {
		super(message);
	}

	public JXPathException(Throwable cause) {
		super(cause);
	}

}
