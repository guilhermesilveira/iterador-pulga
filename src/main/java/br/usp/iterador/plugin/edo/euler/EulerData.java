package br.usp.iterador.plugin.edo.euler;

public class EulerData {

	private String f;

	private String knownSolution;

	private double t0, t1;

	public String getF() {
		return f;
	}

	public void setF(String f) {
		this.f = f;
	}

	public String getKnownSolution() {
		return knownSolution;
	}

	public void setKnownSolution(String knownSolution) {
		this.knownSolution = knownSolution;
	}

	public double getT0() {
		return t0;
	}

	public void setT0(double t0) {
		this.t0 = t0;
	}

	public double getT1() {
		return t1;
	}

	public void setT1(double t1) {
		this.t1 = t1;
	}

}
